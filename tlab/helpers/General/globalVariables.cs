// Get the Screen coords for a 3D position
function scheduleGlobal(%global,%delay,%value) {

	schedule(%delay,"","updateGlobalValue",%global,%value);
}
//------------------------------------------------------------------------------
//==============================================================================
// Get the Screen coords for a 3D position
function updateGlobalValue(%global,%value) {
	eval("$"@%global@" = %value;");
}
